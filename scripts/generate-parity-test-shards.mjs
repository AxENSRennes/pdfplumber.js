import { mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const fixturesRoot = path.join(repoRoot, "test/fixtures/parity-cycles");
const testsRoot = path.join(repoRoot, "test/parity/cycles");

function parseCycles(argv) {
  const index = argv.indexOf("--cycles");
  const value = index === -1 ? "04,05,06" : argv[index + 1];
  if (!value) throw new Error("--cycles requires a comma-separated value");
  const parts = value.split(",").map((part) => part.trim()).filter(Boolean);
  const cycles = parts.map((part) => Number.parseInt(part, 10));
  if (!cycles.length || cycles.some((cycle, index) => !Number.isFinite(cycle) || String(cycle).padStart(2, "0") !== parts[index].padStart(2, "0"))) {
    throw new Error(`--cycles must be a comma-separated list of cycle numbers, received: ${value}`);
  }
  return cycles;
}

function cycleLabel(cycle) {
  return String(cycle).padStart(2, "0");
}

function primaryShardFromId(id) {
  const basename = id.split("/").at(-1) ?? id;
  const match = /^c\d+-\d+-(.+)$/.exec(basename);
  return match?.[1] ?? basename;
}

function sanitizeShardName(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function shardFromEntry(entry) {
  if (entry.sourceDocumentId) {
    return sanitizeShardName(entry.sourceDocumentId);
  }
  return primaryShardFromId(entry.id);
}

function readManifest(cycle, phase) {
  const label = cycleLabel(cycle);
  const manifestPath = path.join(fixturesRoot, `cycle-${label}`, `${phase}-manifest.json`);
  return JSON.parse(readFileSync(manifestPath, "utf8"));
}

function shardSource({ cycle, phase, shard, ids }) {
  return `// Generated by scripts/generate-parity-test-shards.mjs. Do not edit by hand.
import { defineCycleParityShard } from "../../../cycle-shard-runner.js";

const caseIds = ${JSON.stringify(ids, null, 2)};

defineCycleParityShard({
  cycle: ${cycle},
  phase: ${JSON.stringify(phase)},
  shard: ${JSON.stringify(shard)},
  caseIds
});
`;
}

function generatePhase(cycle, phase) {
  const label = cycleLabel(cycle);
  const outDir = path.join(testsRoot, `cycle-${label}`, phase);
  rmSync(outDir, { recursive: true, force: true });
  mkdirSync(outDir, { recursive: true });

  const manifest = readManifest(cycle, phase);
  const groups = new Map();
  for (const entry of manifest) {
    const shard = shardFromEntry(entry);
    const ids = groups.get(shard) ?? [];
    ids.push(entry.id);
    groups.set(shard, ids);
  }

  for (const [shard, ids] of [...groups.entries()].sort(([a], [b]) => a.localeCompare(b))) {
    const filePath = path.join(outDir, `${shard}.test.ts`);
    writeFileSync(filePath, shardSource({ cycle, phase, shard, ids }), "utf8");
  }

  return { phase, shardCount: groups.size, caseCount: manifest.length };
}

const cycles = parseCycles(process.argv.slice(2));
for (const cycle of cycles) {
  const generated = [generatePhase(cycle, "working"), generatePhase(cycle, "holdout")];
  const label = cycleLabel(cycle);
  const summary = generated.map((item) => `${item.phase}: ${item.shardCount} shards / ${item.caseCount} cases`).join(", ");
  console.log(`cycle-${label}: ${summary}`);
}
